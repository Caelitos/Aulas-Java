    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
       while (tabelaModelo.getRowCount() > 0) {
                tabelaModelo.removeRow(0);
       }
        cPessoa.Conectar();
        resultset = cPessoa.Pesquisar(tf_pesquisa.getText());       
        try {
            while (resultset.next()) {
                tabelaModelo.addRow(new String[]{
                resultset.getString("pes_id"),
                resultset.getString("pes_nome")
                });
            }        
        tabelaResultados.setModel(tabelaModelo);
        } catch (SQLException ex) {
            Logger.getLogger(V_Pessoa.class.getName()).log(Level.SEVERE, null, ex);
        }
        cPessoa.Desconectar();
    }//GEN-LAST:event_bt_pesquisarActionPerformed

    private void tabelaResultadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaResultadosMouseClicked
        lb_id.setText((String) tabelaResultados.getValueAt(
                tabelaResultados.getSelectedRow(), 0));
        tf_nome.setText((String) tabelaResultados.getValueAt(
                tabelaResultados.getSelectedRow(), 1));
    }//GEN-LAST:event_tabelaResultadosMouseClicked

